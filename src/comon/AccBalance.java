package comon;

public class AccBalance {

	String AccountId;
	int integerBalance;
	int decimalBalance;

	public String getAccountId() {
		return AccountId;
	}

	public void setAccountId(String accountId) {
		AccountId = accountId;
	}

	public int getIntegerBalance() {
		return integerBalance;
	}

	public void setIntegerBalance(int integerBalance) {
		this.integerBalance = integerBalance;
	}

	public int getDecimalBalance() {
		return decimalBalance;
	}

	public void setDecimalBalance(int decimalBalance) {
		this.decimalBalance = decimalBalance;
	}
}
